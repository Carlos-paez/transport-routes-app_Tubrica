map.on(L.Draw.Event.CREATED, async (e) => {
  const n = prompt("Nombre de la ruta:");
  if (n) {
    const color = getNextRouteColor();
    const latlngs = e.layer.getLatLngs();

    // Determinar si el nombre coincide con una ruta Omega
    const isOmegaName = checkIsOmega(n);
    let passengers = 0;

    // Si es una ruta Omega, intentar obtener los pasajeros del dataset original
    if (isOmegaName) {
      const omegaData = OMEGA_WAYPOINTS.find((w) => w.name.trim() === n.trim());
      if (omegaData) passengers = omegaData.passengers || 0;
    }

    // Verificar si ya existe en la DB para no duplicar
    const res = await fetch("api.php");
    const data = await res.json();
    const existing = data.find(
      (el) => el.name.trim() === n.trim() && el.type === "route",
    );

    let id;
    if (existing) {
      // Actualizar existente
      id = existing.id;
      await fetch(`api.php?id=${id}`, {
        method: "PUT",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({
          geometry: latlngs,
          color: color,
          passengers: passengers,
        }),
      });

      // Eliminar la capa vieja si existe
      if (isOmegaName) {
        const routeIdx = OMEGA_WAYPOINTS.findIndex(
          (w) => w.name.trim() === n.trim(),
        );
        if (routeIdx !== -1 && OMEGA_WAYPOINTS[routeIdx].layer) {
          omegaLayer.removeLayer(OMEGA_WAYPOINTS[routeIdx].layer);
        }
      }
    } else {
      // Crear nuevo
      id = await saveElement(n, "route", latlngs, color, passengers);
    }

    const newLayer = renderRoute(
      latlngs,
      n,
      id,
      null,
      isOmegaName,
      color,
      passengers,
    );

    // Si es Omega, actualizar la referencia en el array
    if (isOmegaName) {
      const routeIdx = OMEGA_WAYPOINTS.findIndex(
        (w) => w.name.trim() === n.trim(),
      );
      if (routeIdx !== -1) OMEGA_WAYPOINTS[routeIdx].layer = newLayer;
    }

    resetModes();
    updateRoutesList();
    setStatus("Ruta guardada: " + n);
  }
});
