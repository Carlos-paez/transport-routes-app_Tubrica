function refreshStats(group) {
    if (group) {
        const lls = group.routeLine.getLatLngs();
        let dist = 0;
        for (let i = 1; i < lls.length; i++) dist += lls[i-1].distanceTo(lls[i]);
        group.totalDistance = dist;
        group.totalTime = (dist / 1000 / 40) * 3600;
    }
    updateLegend();
}
